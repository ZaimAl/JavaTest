package Operation;

public class Relational {
    public static void main(String[] args) {
        Integer[] arrayBilangan = {4, 9, 12, 3, 6};
        System.out.println(arrayBilangan[1]>arrayBilangan[0]);
        System.out.println(arrayBilangan[2]==arrayBilangan[0]);
        System.out.println(arrayBilangan[0]<arrayBilangan[1]);
        System.out.println(arrayBilangan[2]>=arrayBilangan[3]);
        System.out.println(arrayBilangan[4]<=arrayBilangan[1]);
    }
}
